<h1>Starting off with Scaffold</h1>
<p>As this is a PHP framework, we’re going to have to skip things, assume things, and hope things are set up a certain way, otherwise we’d never get anything done. While we are working on getting Scaffold to work on as many servers and setups as possible, a few won‘t work great out of the box.</p>
<p>To check whether your setup will work with Scaffold, visit the <a href="./system-requirements">System Requirements</a> page. It’s good with that sort of thing.</p>

<h2>What we’ll need from you:</h2>
<ol>
	<li>An intermediate to ninja level of PHP knowledge.</li>
	<li>A basic idea of MVC frameworks and how they work (although this isn’t a game-changer).</li>
	<li>An app to build. With Scaffold, it can be nearly anything! Except a time machine.</li>
	<li>Access to change the PHP <a href="./paths"><code>include_path</code></a> value or <code>DocumentRoot</code>.</li>
	<li>Patience, young grasshopper.</li>
</ol>